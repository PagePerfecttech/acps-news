async function seedDatabase() {
    try {
        console.log('üå± Starting database seeding...\n');

        // Check if DATABASE_URL is set
        if (!process.env.DATABASE_URL) {
            console.error('‚ùå DATABASE_URL is not set in .env.local');
            process.exit(1);
        }

        // First, insert categories
        console.log('üìÅ Inserting categories...');
        for (const category of sampleCategories) {
            // Check if category already exists
            const existing = await db.select()
                .from(categories)
                .where(eq(categories.slug, category.slug))
                .limit(1);

            if (existing.length === 0) {
                await db.insert(categories).values(category);
                console.log(`  ‚úì Added category: ${category.name}`);
            } else {
                console.log(`  - Category already exists: ${category.name}`);
            }
        }

        console.log('\nüì∞ Inserting news articles...');
        for (const article of sampleArticles) {
            const result = await db.insert(newsArticles).values(article).returning();
            console.log(`  ‚úì Added article: ${article.title.substring(0, 50)}...`);
            console.log(`     ID: ${result[0].id}`);
        }

        console.log('\n‚úÖ Database seeding completed successfully!\n');
        console.log('Summary:');
        console.log(`  - ${sampleCategories.length} categories`);
        console.log(`  - ${sampleArticles.length} news articles`);
        console.log('\nüåê Visit http://localhost:3000 to see your articles!');

        process.exit(0);
    } catch (error) {
        console.error('‚ùå Error seeding database:', error);
        process.exit(1);
    }
}

